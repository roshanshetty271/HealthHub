     public void populateAnonymousHelpRequestTable(){
         
        DefaultTableModel model = (DefaultTableModel)anonymousHelpReqTable.getModel();
        model.setRowCount(0);
        
        for(RequestFramework requestFramework : volunteerOrganization.getRequestPipeline().getRequestList()){
         if(requestFramework.getReqMessage().equalsIgnoreCase("Need GetHelp"))
         {
            Object[] row = new Object[4];
            row[0] = requestFramework;
            row[1] = requestFramework.getSenderDetails().getPerson().getName();
            row[2] = requestFramework.getReceiverDetails() == null ? null : requestFramework.getReceiverDetails().getPerson().getName();
            row[3] = requestFramework.getStatusOfRequest();
            
            model.addRow(row);
         }
        }
    }

