    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = anonymousHelpReqTable.getSelectedRow();

        if (selectedRow < 0){
        JOptionPane.showMessageDialog(null, "Please select a row", "warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
      
        RequestFramework requestFramework = (RequestFramework)anonymousHelpReqTable.getValueAt(selectedRow, 0);
        if(requestFramework.getReceiverDetails()!=null)
        {
         JOptionPane.showMessageDialog(null, "RequestFramework has been processed by Other Volunteers!", "warning", JOptionPane.WARNING_MESSAGE);
         return;     
        }
        requestFramework.setReceiver(usersAccount);
        requestFramework.setStatus("Pending");
        populateAnonymousHelpRequestTable();

    }//GEN-LAST:event_assignJButtonActionPerformed

