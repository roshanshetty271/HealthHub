    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // Get users name
        String userName = userNameJTextField.getText();
        // Get Password
        char[] passwordCharArray = passwordField.getPassword();
        String password = String.valueOf(passwordCharArray);

        //Step1: Check in the system users account directory if you have the users
        Users users = system.getUserDirectory().validateUser(userName, password);
        Enterprise inEnterprise = null;
        Org inOrganization = null;
        if (users == null) {
            //Step2: Go inside each Network to check each enterprise
            for (Network network : system.getNetworkList()) {
                //Step 2-a: Check against each enterprise
                for (Enterprise enterprise : network.getEnterprisesDirectory().getEnterprisesList()) {
                    users = enterprise.getUserDirectory().validateUser(userName, password);
                    if (users == null ) {
                        //Step3: Check against each organization inside that enterprise
                        for (Org organization : enterprise.getOrganizationsDirectory().getOrganizationsList()) {
                            users = organization.getUserDirectory().validateUser(userName, password);
                            if (users != null ) {
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                break;
                            }
                        }
                    } else {
                        inEnterprise = enterprise;
                        break;
                    }
                    if (inOrganization != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }
       
        if (users == null) {
            JOptionPane.showMessageDialog(null, "Invalid Credentails!");
            return;
        } 
         if(!users.isEnabled())
        {
        JOptionPane.showMessageDialog(null, "Invalid Credentails! Your account is not enabled. Please contact the Manager.");
            return;    
        }
        else {
            CardLayout layout = (CardLayout) container.getLayout();
            container.add("workArea", users.getRole().createWorkArea(container, users, inOrganization, inEnterprise, system));
            layout.next(container);
        }
        loginJButton.setEnabled(false);
        logoutJButton.setEnabled(true);
        userNameJTextField.setEnabled(false);
        passwordField.setEnabled(false);
    }//GEN-LAST:event_loginJButtonActionPerformed

