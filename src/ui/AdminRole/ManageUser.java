    private void createUserJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserJButtonActionPerformed
       try
       {
        String userName = userNameJTextField.getText();
        String password = passwordJTextField.getText();
        Org org = (Org) organizationJComboBox.getSelectedItem();
        Person person = (Person) employeeJComboBox.getSelectedItem();
        Roles roles = (Roles) roleJComboBox.getSelectedItem();
        for(Users  user : org.getUserDirectory().getUserList())
        {
            if(userName.equalsIgnoreCase( user.getName()))
            {
            JOptionPane.showMessageDialog(null, "Users Name already exists!!", "warning", JOptionPane.WARNING_MESSAGE);
            return;
            }
        }
        Users account = org.getUserDirectory().addUser(userName, password, person, roles);
        account.setNetwork((Network) users.getNetwork());
        account.setEnabled(true);
        popOrganizationUserAccountTable();
        JOptionPane.showMessageDialog(null, " Users Account has been created successfully", "success",JOptionPane.PLAIN_MESSAGE);
        resetFields();
       }
       catch(Exception e)
       {
        JOptionPane.showMessageDialog(this, "Please enter valid data", "warning",JOptionPane.WARNING_MESSAGE);    
       }
    }//GEN-LAST:event_createUserJButtonActionPerformed

