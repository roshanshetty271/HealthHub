    public void populatehelpRequestTable()
    {
        DefaultTableModel model = (DefaultTableModel)helpRequestListTable.getModel();
        
        model.setRowCount(0);
         DateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy"); 
        for(RequestFramework requestFramework : usersAccount.getRequestPipeline().getRequestList()){
          if(requestFramework.getReqMessage().equalsIgnoreCase("Need GetHelp"))
          {
            Object[] row = new Object[4];
            row[0] = requestFramework;
            row[1] = requestFramework.getSenderDetails();
            row[2] = dateFormat.format(requestFramework.getRequestDate());
            row[3] = requestFramework.getStatusOfRequest();
           
            model.addRow(row);
          }
          if((requestFramework.getReqMessage().equals("RequestFramework for Approval")))
          {
            String msg = ("Your account is approved by").concat(requestFramework.getReceiverDetails().getPerson().getName());
           accountTxtAreaField.setText(msg);
           accountTxtAreaField.setEditable(false);
            }
          }
        
    }
    
