    public void popOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Org org : enterprise.getOrganizationsDirectory().getOrganizationsList()) {
            organizationJComboBox.addItem(org);
        }
    }
     public void populateOrganizationComboBox(){
        organizationJComboBox.removeAllItems();
        
        for(Org org :  enterprise.getOrganizationsDirectory().getOrganizationsList()){
            if(!(org.getName().equals("Helpers Organization")) &&
                    !(org.getName().equals("Admin")) &&
                    !(org.getName().equals("HelpSeeker Organization")))
            {
            organizationJComboBox.addItem(org);
            }
        }
    }
     
    public void populateEmployeeComboBox(Org org){
        employeeJComboBox.removeAllItems();
        
       if(!(org.getName().equals("Helpers Organization")) &&
                    !(org.getName().equals("Admin")) &&
                    !(org.getName().equals("HelpSeeker Organization")))
          {
          for (Person employee : org.getPersonDirectory().getPersonList()){
            employeeJComboBox.addItem(employee);   
          }
        }
     }
    
    private void populateRoleComboBox(Org org){
        roleJComboBox.removeAllItems();
        for (Roles roles : org.getSupportedRole()){
            roleJComboBox.addItem(roles);
        }
    }

    public void popOrganizationUserAccountTable() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (Org org : enterprise.getOrganizationsDirectory().getOrganizationsList()) {
            for (Users  user : org.getUserDirectory().getUserList()) {
                Object row[] = new Object[2];
                row[0] =  user;
                row[1] =  user.getRole();
                
                model.addRow(row);
            }
        }
    }

